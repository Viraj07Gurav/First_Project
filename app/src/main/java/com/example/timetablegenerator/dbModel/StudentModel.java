package com.example.timetablegenerator.dbModel;

public class StudentModel {
    String studFullName, studUname, studPwd, studDept, studSect;

    public String getStudFullName() {
        return studFullName;
    }

    public void setStudFullName(String studFullName) {
        this.studFullName = studFullName;
    }

    public String getStudUname() {
        return studUname;
    }

    public void setStudUname(String studUname) {
        this.studUname = studUname;
    }

    public String getStudPwd() {
        return studPwd;
    }

    public void setStudPwd(String studPwd) {
        this.studPwd = studPwd;
    }

    public String getStudDept() {
        return studDept;
    }

    public void setStudDept(String studDept) {
        this.studDept = studDept;
    }

    public String getStudSect() {
        return studSect;
    }

    public void setStudSect(String studSect) {
        this.studSect = studSect;
    }
}
